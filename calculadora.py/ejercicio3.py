# Programa: Intercambio de valores

# Declaración de variables
a = 5
b = 10

# Mostrar los valores originales
print("===== ANTES DEL INTERCAMBIO =====")
print("Valor de a:", a)
print("Valor de b:", b)

# Intercambiar los valores utilizando una variable auxiliar
auxiliar = a
a = b
b = auxiliar

# Mostrar los nuevos valores
print("\n===== DESPUÉS DEL INTERCAMBIO =====")
print("Valor de a:", a)
print("Valor de b:", b)